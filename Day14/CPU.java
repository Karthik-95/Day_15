package Day14;

public class CPU {
	private String brand;
	private int rom;
	private int ram;
	public void setBrand(String brand) {
		this.brand=brand;
	}
	public String getBrand() {
		return this.brand;
	}
	public void setRom(int rom) {
		this.rom=rom;
	}
	public int getRom() {
		return this.ram;
	}
	
	public void setRam(int ram) {
		this.ram=ram;
	}
	public int getRam() {
		return this.ram;
	}

}
